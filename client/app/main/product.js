// product class
function product(id, name, description, price, thumbnail, image, reviewAvg, reviewCount) {
    this.id = id; 
    this.name = name;
    this.description = description;
    this.price = price;
    this.thumbnail = thumbnail;
    this.image = image;
    this.reviewAvg = reviewAvg;
    this.reviewCount = reviewCount;

}